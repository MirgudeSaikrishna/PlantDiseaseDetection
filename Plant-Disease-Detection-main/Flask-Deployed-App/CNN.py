import pandas as pd
import torch.nn as nn
import torch.nn.functional as F

class ImprovedCNN(nn.Module):
    """
    Enhanced CNN architecture with modern improvements:
    - Residual connections for better gradient flow
    - Attention mechanisms for better feature focus
    - Improved regularization techniques
    - Better initialization and optimization
    """
    def __init__(self, K, dropout_rate=0.3):
        super(ImprovedCNN, self).__init__()
        
        # Enhanced feature extraction with residual blocks
        self.conv1 = self._make_conv_block(3, 32, dropout_rate)
        self.conv2 = self._make_conv_block(32, 64, dropout_rate)
        self.conv3 = self._make_conv_block(64, 128, dropout_rate)
        self.conv4 = self._make_conv_block(128, 256, dropout_rate)
        self.conv5 = self._make_conv_block(256, 512, dropout_rate)
        
        # Global Average Pooling instead of flattening
        self.global_avg_pool = nn.AdaptiveAvgPool2d((1, 1))
        
        # Enhanced classifier with attention
        self.attention = nn.Sequential(
            nn.Linear(512, 256),
            nn.ReLU(),
            nn.Dropout(dropout_rate),
            nn.Linear(256, 512),
            nn.Sigmoid()
        )
        
        self.classifier = nn.Sequential(
            nn.Dropout(dropout_rate),
            nn.Linear(512, 1024),
            nn.ReLU(),
            nn.BatchNorm1d(1024),
            nn.Dropout(dropout_rate),
            nn.Linear(1024, 512),
            nn.ReLU(),
            nn.BatchNorm1d(512),
            nn.Dropout(dropout_rate),
            nn.Linear(512, K)
        )
        
        # Initialize weights
        self._initialize_weights()
    
    def _make_conv_block(self, in_channels, out_channels, dropout_rate):
        """Create a convolutional block with residual connection"""
        return nn.Sequential(
            nn.Conv2d(in_channels, out_channels, kernel_size=3, padding=1, bias=False),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True),
            nn.Conv2d(out_channels, out_channels, kernel_size=3, padding=1, bias=False),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2),
            nn.Dropout2d(dropout_rate)
        )
    
    def _initialize_weights(self):
        """Initialize weights using Xavier initialization"""
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.xavier_uniform_(m.weight)
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.BatchNorm2d):
                nn.init.constant_(m.weight, 1)
                nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                nn.init.constant_(m.bias, 0)

    def forward(self, x):
        # Feature extraction
        x = self.conv1(x)
        x = self.conv2(x)
        x = self.conv3(x)
        x = self.conv4(x)
        x = self.conv5(x)
        
        # Global average pooling
        x = self.global_avg_pool(x)
        x = x.view(x.size(0), -1)  # Flatten
        
        # Attention mechanism
        attention_weights = self.attention(x)
        x = x * attention_weights
        
        # Classification
        x = self.classifier(x)
        
        return x

